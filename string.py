text = "hello"

text2 = "THERE"

# Accessing an element in a string
print(text[1])

# Size of a string
print(len(text2))

# Modifying a string
print(text.upper())  # Writing a string in capital letters

print(text2.lower())  # Writing a string in small letters

# String concatenation - combining  2 or more strings

print(text+text2)


print(text+" "+text2)  # Making a space between the two words

